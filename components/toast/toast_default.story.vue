<template>
  <div>
    <dt-button @click="buttonClicked">
      Click to show!
    </dt-button>

    <dt-toast
      ref="toast"
      :kind="$attrs.kind"
      :title="$attrs.title"
      :message="$attrs.message"
      :title-id="$attrs.titleId"
      :content-id="$attrs.contentId"
      :important="$attrs.important"
      :hide-close="$attrs.hideClose"
      :duration="$attrs.duration"
      :close-button-props="buttonCloseProps"
      @close="closeToast(); $attrs.onClose($event)"
    >
      <span
        v-if="defaultSlot"
        v-html="defaultSlot"
      />
      <span v-else>
        Message body with
        <a
          href="#"
          class="d-link"
          :class="linkClass"
        >a link</a>.
      </span>

      <template #action>
        <span
          v-if="$attrs.action"
          v-html="$attrs.action"
        />
        <dt-button
          v-else
          size="sm"
          importance="outlined"
          :kind="buttonKind"
          @click="$attrs.onClick"
        >
          Action
        </dt-button>
      </template>
      <template
        v-if="$attrs.icon"
        #icon
      >
        <component :is="$attrs.icon" />
      </template>
      <template
        v-if="$attrs.titleOverride"
        #titleOverride
      >
        <span v-html="$attrs.titleOverride" />
      </template>
    </dt-toast>
  </div>
</template>

<script>
import DtToast from './toast';
import DtButton from '../button/button';
import icon from '@/common/mixins/icon';

export default {
  name: 'ToastDefault',

  components: { DtToast, DtButton },

  mixins: [icon],

  computed: {
    shouldInvertButton () {
      return this.kind === 'base' || this.kind === 'error' || this.kind === 'info';
    },

    isInverted () {
      return this.$attrs.important && this.shouldInvertButton;
    },

    buttonKind () {
      return this.isInverted ? 'inverted' : 'muted';
    },

    linkClass () {
      return this.isInverted ? 'd-link--inverted' : 'd-link--muted';
    },

    buttonCloseProps () {
      return {
        ...this.$attrs.closeButtonProps,
        kind: this.buttonKind,
        ariaLabel: 'Close',
      };
    },
  },

  methods: {
    buttonClicked () {
      this.$refs.toast.show();
    },

    closeToast () {
      this.$refs.toast.close();
    },
  },
};
</script>
