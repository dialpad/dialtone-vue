<template>
  <dt-dropdown
    :items="items"
    :open="isOpen"
    :fixed-alignment="fixedAlignment"
    :content-width="contentWidth"
    :padding="padding"
    @highlight="onHighlight"
    @select="onDropdownSelect"
    @escape="onDropdownEscape"
    :navigation-type="navigationType"
    @update:open="updateOpen"
  >
    <template #anchor="{ attrs }">
      <div
        v-if="anchor"
        v-html="anchor">
      </div>
      <dt-button
        v-else
        v-bind="attrs"
        @click.prevent="isOpen = !isOpen"
      >
        Click to open
      </dt-button>
    </template>
    <template #list="{ listProps, getItemProps, activeItemIndex, setHighlightIndex }">
      <div
        v-if="list"
        v-html="list"
      ></div>
      <ul
        v-else
        v-bind="listProps"
        class="d-p0"
      >
        <dt-list-item
          v-for="(item, i) in items"
          v-bind="getItemProps(i)"
          :key="item.id"
          :is-highlighted="activeItemIndex === i"
          :set-highlight="() => setHighlightIndex(i)"
          :navigation-type="navigationType"
          :focusable="true"
          @click="onDropdownSelect(i)"
        >
          {{ item.name }}
        </dt-list-item>
      </ul>
    </template>
  </dt-dropdown>
</template>

<script>
import DtDropdown from './dropdown';
import { DtListItem } from '../list_item';
import { DtButton } from '../button';

export default {
  name: 'DtDropdownDefault',

  components: { DtDropdown, DtListItem, DtButton },

  data () {
    return {
      isOpen: this.open || false,
    };
  },

  watch: {
    open: {
      handler () {
        this.isOpen = this.open;
      },
    },
  },

  methods: {
    updateOpen (isOpen) {
      this.isOpen = isOpen;
    },

    onDropdownSelect (i) {
      this.isOpen = false;
      this.onSelect(i);
    },

    onDropdownEscape () {
      this.isOpen = false;
      this.onEscape();
    },
  },
};
</script>
