<template>
  <div
    :id="`dt-panel-${id}`"
    role="tabpanel"
    tabindex="0"
    :aria-labelledby="`dt-tab-${tabId}`"
    :aria-hidden="`${hidePanel}`"
    :class="[
      {
        'd-d-none': hidePanel,
      },
      tabPanelClass,
    ]"
    data-qa="dt-tab-panel"
  >
    <!-- @slot default slot with panel content -->
    <slot v-show="!hidden" />
  </div>
</template>

<script>
export default {
  name: 'DtTabPanel',

  inject: ['groupContext'],
  props: {
    /**
     * Id of the panel
     */
    id: {
      type: String,
      required: true,
    },

    /**
     * Id of the associated tab
     */
    tabId: {
      type: String,
      required: true,
    },

    /**
    * If true, hides the tab content
    */
    hidden: {
      type: Boolean,
      default: false,
    },

    /**
     * Used to customize the tab element
     */
    tabPanelClass: {
      type: [String, Array, Object],
      default: '',
    },
  },

  computed: {
    hidePanel () {
      return this.groupContext.selected !== this.id || this.hidden;
    },
  },
};
</script>
